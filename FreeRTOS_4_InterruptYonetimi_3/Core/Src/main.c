/* USER CODE BEGIN 4 */
void EXTI0_IRQHandler(void)
{
	uint32_t ulReceivedNumber;

	static const char *pcStrings[] =
	{
	"String 0\r\n",
	"String 1\r\n",
	"String 2\r\n",
	"String 3\r\n"
	};

	if(__HAL_GPIO_EXTI_GET_IT(GPIO_PIN_0) != 0)
	{
		BaseType_t xHigherPriorityTaskWoken = pdFALSE;
		UartPrint("ISR is running...\r\n");

		while( xQueueReceiveFromISR( xIntegerQueue, &ulReceivedNumber, &xHigherPriorityTaskWoken ) != errQUEUE_EMPTY )
		{
			ulReceivedNumber &= 0x03;
			xQueueSendToBackFromISR( xStringQueue,&pcStrings[ ulReceivedNumber ],&xHigherPriorityTaskWoken );
		}

		__HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_0);
		portYIELD_FROM_ISR( xHigherPriorityTaskWoken );
	}

}

void UartPrint(char* message)
{
	HAL_UART_Transmit(&huart2, (uint8_t*)message, strlen(message), 20);
}
